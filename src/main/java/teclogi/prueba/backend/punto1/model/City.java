package teclogi.prueba.backend.punto1.model;

public class City {
    private Cordenadas coord;
    private Weather[] weather;
    private String base;
    private Main main;
    private int visibility;
    private Wind wind;
    private Clouds clouds;
    private int dt;
    private Sys sys;
    private int timezone;
    private int id;
    private String name;
    private int cod;

    /**
     * Constructor de la clase City
     * @param coord Coordenadas de la ciudad
     * @param weather Clima de la ciudad
     * @param base informacion de la ciudad
     * @param main informacion de la ciudad
     * @param visibility informacion de la ciudad
     * @param wind informacion de la ciudad
     * @param clouds informacion de la ciudad
     * @param dt informacion de la ciudad
     * @param sys informacion de la ciudad
     * @param timezone informacion de la ciudad
     * @param id informacion de la ciudad
     * @param name informacion de la ciudad
     * @param cod informacion de la ciudad
     */

    public City(Cordenadas coord, Weather[] weather, String base, Main main, int visibility, Wind wind, Clouds clouds,
                int dt, Sys sys, int timezone, int id, String name, int cod) {
        super();
        this.coord = coord;
        this.weather = weather;
        this.base = base;
        this.main = main;
        this.visibility = visibility;
        this.wind = wind;
        this.clouds = clouds;
        this.dt = dt;
        this.sys = sys;
        this.timezone = timezone;
        this.id = id;
        this.name = name;
        this.cod = cod;
    }


    public Cordenadas getCoord() {
        return coord;
    }

    public void setCoord(Cordenadas coord) {
        this.coord = coord;
    }

    public Weather[] getWeather() {
        return weather;
    }

    public void setWeather(Weather[] weather) {
        this.weather = weather;
    }

    public String getBase() {
        return base;
    }

    public void setBase(String base) {
        this.base = base;
    }

    public Main getMain() {
        return main;
    }

    public void setMain(Main main) {
        this.main = main;
    }

    public int getVisibility() {
        return visibility;
    }

    public void setVisibility(int visibility) {
        this.visibility = visibility;
    }

    public Wind getWind() {
        return wind;
    }

    public void setWind(Wind wind) {
        this.wind = wind;
    }

    public Clouds getClouds() {
        return clouds;
    }

    public void setClouds(Clouds clouds) {
        this.clouds = clouds;
    }

    public int getDt() {
        return dt;
    }

    public void setDt(int dt) {
        this.dt = dt;
    }

    public Sys getSys() {
        return sys;
    }

    public void setSys(Sys sys) {
        this.sys = sys;
    }

    public int getTimezone() {
        return timezone;
    }

    public void setTimezone(int timezone) {
        this.timezone = timezone;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getCod() {
        return cod;
    }

    public void setCod(int cod) {
        this.cod = cod;
    }
}